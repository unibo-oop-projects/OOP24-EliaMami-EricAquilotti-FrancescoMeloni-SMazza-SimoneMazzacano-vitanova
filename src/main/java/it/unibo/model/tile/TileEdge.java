package it.unibo.model.tile;

/**
 * Enum representing all types of edges.
 */
public enum TileEdge {
    /**
     * Grass edge.
     */
    EDGE_GRASS,
    /**
     * Water edge.
     */
    EDGE_WATER,
    /**
     * Upper coast edge.
     */
    EDGE_COAST_UP,
    /**
     * Right coast edge.
     */
    EDGE_COAST_RIGHT,
    /**
     * Down coast edge.
     */
    EDGE_COAST_DOWN,
    /**
     * Left coast edge.
     */
    EDGE_COAST_LEFT;
}
