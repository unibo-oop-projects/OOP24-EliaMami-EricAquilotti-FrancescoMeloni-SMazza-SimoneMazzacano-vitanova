package it.unibo.common;

/**
 * Enum that represents the state of a chapter.
 */
public enum ChapterState {
    /**
     * The player has won the game.
     */
    PLAYER_WON,
    /**
     * The player has lost the game.
     */
    PLAYER_LOST,
    /**
     * The game is in progress.
     */
    IN_PROGRESS;
}
